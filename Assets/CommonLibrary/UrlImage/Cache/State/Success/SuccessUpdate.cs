using UnityEngine;
using System.Collections;
using System.Collections.Generic;

namespace Images.Cache
{
    public class SuccessUpdate : UpdateBehavior<Success>
    {

        #region Update

        public override void update()
        {
            if (dirty)
            {
                dirty = false;
                if (this.data != null)
                {
                    ImageLoader imageLoader = this.data.findDataInParent<ImageLoader>();
                    if (imageLoader != null)
                    {
                        imageLoader.changeIndex.v++;
                    }
                    else
                    {
                        Logger.LogError("imageLoader null");
                    }
                }
                else
                {
                    Logger.LogError("data null");
                }
            }
        }

        public override bool isShouldDisableUpdate()
        {
            return true;
        }

        #endregion

        #region implement callBacks

        public override void onAddCallBack<T>(T data)
        {
            if (data is Success)
            {
                dirty = true;
                return;
            }
            Logger.LogError("Don't process: " + data + "; " + this);
        }

        public override void onRemoveCallBack<T>(T data, bool isHide)
        {
            if (data is Success)
            {
                Success success = data as Success;
                this.setDataNull(success);
                return;
            }
            Logger.LogError("Don't process: " + data + "; " + this);
        }

        public override void onUpdateSync<T>(WrapProperty wrapProperty, List<Sync<T>> syncs)
        {
            if (WrapProperty.checkError(wrapProperty))
            {
                return;
            }
            if (wrapProperty.p is Success)
            {
                switch ((Success.Property)wrapProperty.n)
                {
                    case Success.Property.texture:
                        break;
                    default:
                        Logger.LogError("Don't process: " + wrapProperty + "; " + this);
                        break;
                }
                return;
            }
            Logger.LogError("Don't process: " + wrapProperty + "; " + syncs + "; " + this);
        }

        #endregion

    }
}